class SkierSnowboarder:
    def __init__(self, name, age, skillLevel, length, price, howFar):
      self.name = name
      self.age = age
      self.skillLevel = skillLevel
      self.length = length
      self.price = price
      self.howFar = howFar
    
    def getName(self):
      return self.name
    
    def getAge(self):
      return self.age
    
    def getSkillLevel(self):
      return self.skillLevel
    
    def getLength(self):
      return self.length
    
    def getPrice(self):
      return self.price
    
    def getHowFar(self):
      return self.howFar
    
    

