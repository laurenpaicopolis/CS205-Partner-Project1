class Trail:
    def __init__(self, name, levelTrail, nightSkiing):
      self.name = name
      self.levelTrail = levelTrail
      self.nightSkiing = nightSkiing

    def getName(self):
      return self.name

    def getLevelTrail(self):
      return self.levelTrail

    def getNightSkiing(self):
      return self.nightSkiing